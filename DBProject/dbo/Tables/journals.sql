CREATE TABLE [dbo].[journals] (
    [JOUR_ID]      INT            NOT NULL,
    [JOURNAL_NAME] NVARCHAR (255) NOT NULL,
    [TIME_ID]      INT            NOT NULL,
    [MONTH]        INT            NOT NULL,
    [VOLUME]       NVARCHAR (255) NOT NULL,
    [YEAR]         INT            NOT NULL,
    [EST]          INT            NOT NULL
);

